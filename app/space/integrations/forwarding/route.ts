export async function POST(req: NextRequest) {
  try {
    const params = await getApiParams(
      req,
      [
        { name: "from", required: true },
        { name: "subject", required: true },
        { name: "body", required: true },
      ],
      { type: "BODY" }
    );

    const user = await prisma.user.findFirst({
      where: { email: params.from },
      select: {
        id: true,
        spaces: {
          where: { selected: true },
          select: { spaceId: true },
          take: 1,
        },
        timeZone: true,
        profile: { select: { name: true } },
      },
    });

    if (!user) {
      return Response.json({
        success: false,
        subject: "ðŸ˜” Oh no! We couldn't find your account.",
        body: render(
          ForwardedEmail({
            error: "We couldn't find your account.",
            toEmail: params.from,
          })
        ),
      });
    }

    const note = await getDescription(params.body);

    const data = await prisma.entity.create({
      data: {
        type: "TASK",
        name: params.subject,
        integrationParams: {
          type: "EMAIL_FORWARD",
          from: params.from,
          icon: "mail",
        },
        pinned:
          [
            "urgent",
            "important",
            "asap",
            "as soon as possible",
            "soon",
            "quick",
            "critical",
            "required",
            "action",
            "âš ï¸",
            "ðŸš¨",
          ].map((t) => params.subject.toLowerCase().includes(t)).length > 0,
        note,
        hasSimplifiedNote: note !== params.body,
        shortId: generateRandomString(6),
        start: dayjs()
          .startOf("day")
          .utc()
          .tz(user.timeZone || "UTC")
          .toDate(),
        published: true,
        space: { connect: { id: user.spaces[0].spaceId } },
      },
    });

    if (process.env.NODE_ENV === "production")
      new Notification("EMAIL_FORWARDING", {
        title: "ðŸŽ‰ We recieved your email!",
        body: "We've added a task to your to-do list!",
      }).dispatch(user.id);

    return Response.json({
      success: true,
      subject: "ðŸŽ‰ #dysperse added your email as a task!",
      body: render(
        ForwardedEmail({
          toEmail: params.from,
          toName: (user.profile as any).name,
          shortId: (data as any).shortId,
          taskData: data,
        })
      ),
    });
  } catch (e) {
    return handleApiError(e);
  }
}

